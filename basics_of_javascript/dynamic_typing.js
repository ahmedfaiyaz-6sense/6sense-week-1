/// there are two type
/// of typing system in
/// programming languages
/// ---> static typing
//// --> dynamic typing
/// Javascript is dynamically typed language
/// which type of variable can change on runtime
/// based on the values assigned to them
//// we have two categories of types
//// --> primitive and reference type
    /// primitive type--> string ,number, undefined, null
    // reference type --> objects function collections functions
let some_variable=1
console.log(typeof(some_variable)) // number 
some_variable="1"
console.log(typeof(some_variable)) // string
some_variable=1.1
console.log(typeof(some_variable)) // floating point is also Number
some_variable=undefined
console.log(typeof(some_variable)) // undefined type


